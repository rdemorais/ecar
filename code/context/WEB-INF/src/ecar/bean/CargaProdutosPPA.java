package ecar.bean;


/**
 * Bean contendo as informações para Carga dos Produtos do PPA.<br>
 * 
 * @author aleixo
 */

public class CargaProdutosPPA {
	
	private String anoExe;
	private String siglaAcao;
	private String siglaProduto;
	private String indObra;
	private String nomeIndicador;
	private String nomeProduto;
	private String unidadeIndicador;
	private String valorPrevisto;
	private String codRegiao;
	private String codMunicipio;
	
	/**
	 * @return the anoExe
	 */
	public String getAnoExe() {
		return anoExe;
	}
	/**
	 * @param anoExe the anoExe to set
	 */
	public void setAnoExe(String anoExe) {
		this.anoExe = anoExe;
	}
	/**
	 * @return the codMunicipio
	 */
	public String getCodMunicipio() {
		return codMunicipio;
	}
	/**
	 * @param codMunicipio the codMunicipio to set
	 */
	public void setCodMunicipio(String codMunicipio) {
		this.codMunicipio = codMunicipio;
	}
	/**
	 * @return the codRegiao
	 */
	public String getCodRegiao() {
		return codRegiao;
	}
	/**
	 * @param codRegiao the codRegiao to set
	 */
	public void setCodRegiao(String codRegiao) {
		this.codRegiao = codRegiao;
	}
	/**
	 * @return the indObra
	 */
	public String getIndObra() {
		return indObra;
	}
	/**
	 * @param indObra the indObra to set
	 */
	public void setIndObra(String indObra) {
		this.indObra = indObra;
	}
	/**
	 * @return the nomeIndicador
	 */
	public String getNomeIndicador() {
		return nomeIndicador;
	}
	/**
	 * @param nomeIndicador the nomeIndicador to set
	 */
	public void setNomeIndicador(String nomeIndicador) {
		this.nomeIndicador = nomeIndicador;
	}
	/**
	 * @return the nomeProduto
	 */
	public String getNomeProduto() {
		return nomeProduto;
	}
	/**
	 * @param nomeProduto the nomeProduto to set
	 */
	public void setNomeProduto(String nomeProduto) {
		this.nomeProduto = nomeProduto;
	}
	/**
	 * @return the siglaAcao
	 */
	public String getSiglaAcao() {
		return siglaAcao;
	}
	/**
	 * @param siglaAcao the siglaAcao to set
	 */
	public void setSiglaAcao(String siglaAcao) {
		this.siglaAcao = siglaAcao;
	}
	/**
	 * @return the siglaProduto
	 */
	public String getSiglaProduto() {
		return siglaProduto;
	}
	/**
	 * @param siglaProduto the siglaProduto to set
	 */
	public void setSiglaProduto(String siglaProduto) {
		this.siglaProduto = siglaProduto;
	}
	/**
	 * @return the unidadeIndicador
	 */
	public String getUnidadeIndicador() {
		return unidadeIndicador;
	}
	/**
	 * @param unidadeIndicador the unidadeIndicador to set
	 */
	public void setUnidadeIndicador(String unidadeIndicador) {
		this.unidadeIndicador = unidadeIndicador;
	}
	/**
	 * @return the valorPrevisto
	 */
	public String getValorPrevisto() {
		return valorPrevisto;
	}
	/**
	 * @param valorPrevisto the valorPrevisto to set
	 */
	public void setValorPrevisto(String valorPrevisto) {
		this.valorPrevisto = valorPrevisto;
	}
	
}
